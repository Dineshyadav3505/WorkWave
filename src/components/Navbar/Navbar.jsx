"use client";
import React, { useState, useEffect } from "react";
import { usePathname } from "next/navigation";
import Link from "next/link";
import Logo from "../Logo";
import SocialMedia from "../SocialMedia";
import Button from "../Button";
import { motion, spring } from "framer-motion";

const Navbar = () => {
  const [theme, setTheme] = useState("light");
  const [isMenuOpen, setIsMenuOpen] = useState(false);
  const pathname = usePathname();

  useEffect(() => {
    const storedTheme = localStorage.getItem("theme");
    if (storedTheme) {
      setTheme(storedTheme);
      document.documentElement.classList.toggle("dark", storedTheme === "dark");
    }
  }, []);

  const toggleMenu = () => setIsMenuOpen((prev) => !prev);

  const changeTheme = () => {
    const newTheme = theme === "light" ? "dark" : "light";
    document.documentElement.classList.toggle("dark", newTheme === "dark");
    localStorage.setItem("theme", newTheme);
    setTheme(newTheme);
  };

  return (
    <nav className="h-16 bg-[#023E8A] sticky z-20 top-0 w-full right-0 dark:bg-[#000000] flex justify-between items-center px-[17px]">
      <div className="md:hidden pt-2">
        <button
          onClick={changeTheme}
          aria-label="Toggle theme"
          className="focus:outline-none text-[#FFFFFF]"
        >
          <ThemeToggleIcon theme={theme} />
        </button>
      </div>

      <Logo />

      <div className="hidden md:flex justify-between gap-3 items-center text-sm font-normal">
        {links.map(({ name, url }) => (
          <a
            key={url}
            href={url}
            className={`${
              pathname === url
                ? " font-bold underline text-[#ffffff]"
                : "text-[#D7D5D5]"
            }`}
          >
            {name}
          </a>
        ))}
      </div>

      <div className="flex gap-4">
        <div className="hidden md:flex">
          <button
            onClick={changeTheme}
            aria-label="Toggle theme"
            className="focus:outline-none text-[#FFFFFF]"
          >
            <ThemeToggleIcon theme={theme} />
          </button>
        </div>

        <div onClick={toggleMenu} className="text-[#FFFFFF]">
          <MenuIcon />
        </div>
      </div>

      {/* md Menu */}
      {isMenuOpen && (
        <motion.div
          initial={{ width: 0 }}
          animate={{ width: "auto" }}
          transition={{ duration: 0.5 }}
          className=" absolute flex flex-col min-h-96 items-end  dark:bg-[#000000] bg-[#023E8A] top-0 right-0 py-[20px] px-[17px] pb-16 rounded-bl-lg"
        >
          <motion.div
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            transition={{ duration: 0.5, delay: 0.5 }}
            className="flex flex-col justify-center items-end"
          >
            <div onClick={toggleMenu} className="w-fit text-[#FFFFFF] mb-5">
              <CloseIcon onClick={toggleMenu} />
            </div>

            {menuLinks.map(({ name, url, icon }) => (
              <Link
                href={url}
                className="flex w-64 md:w-[455px] text-[#ffffff] justify-between items-center border-b-[1px]  border-[#E9E5E5] py-3 "
              >
                <span className=" flex gap-2 items-center">
                  <span>{icon}</span>
                  <h1 className=" capitalize text-sm">{name}</h1>
                </span>
                <span>
                  <svg
                    xmlns="http://www.w3.org/2000/svg"
                    fill="none"
                    viewBox="0 0 24 24"
                    strokeWidth={1.5}
                    stroke="currentColor"
                    className="size-5"
                  >
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      d="m8.25 4.5 7.5 7.5-7.5 7.5"
                    />
                  </svg>
                </span>
              </Link>
            ))}

            <Button
              link="sign-in"
              text="Sign In"
              innerClass={
                " font-bold py-1.5 px-11 dark:bg-[#023E8A] bg-[#ffffff]"
              }
            />

            {/* Social Media */}
            <SocialMedia />
          </motion.div>
        </motion.div>
      )}

      {/* Phone Menu */}
      <div className="h-20 md:hidden w-full flex justify-around items-center fixed left-0 right-0 bottom-2 rounded-full dark:bg-[#000000] bg-[#FFFFFF] px-1">
        {links.map(({ name, url, icon }) => (
          <Link
            href={url}
            key={url}
            className="flex flex-col justify-center items-center text-xs gap-1"
          >
            {icon}
            {name}
          </Link>
        ))}
      </div>
    </nav>
  );
};

export default Navbar;

